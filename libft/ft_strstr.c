#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int m;
	int m2;
	int a;

	m = 0;
	a = 0;
	if (s2[a] == '\0')
		return ((char*)s1);
	while (s1[m] != '\0')
	{
		m2 = m;
		while (s1[m2] == s2[a])
		{
			m2++;
			a++;
			if (s2[a] == '\0')
				return (char*)(&s1[m]);
		}
		m++;
		a = 0;
	}
	return (NULL);
}
