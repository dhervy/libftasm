#include "libft.h"

void	ft_putnbr(int n)
{
	int i;

	i = 0;
	if (n == -2147483648)
	{
		i = 1;
		n = n / 10;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = n * -1;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + 48);
	if (i == 1)
		ft_putchar(56);
}
